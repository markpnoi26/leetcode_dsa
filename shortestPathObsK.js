const shortestPath = (grid, k) => {
    
    const targetRow = grid.length-1
    const targetCol = grid[0].length-1
    const rowMax = grid.length
    const colMax = grid[0].length
    const queue = []
    const directions = [[1,0], [0,1], [-1,0], [0,-1]]
    const uniquePaths = {}

    queue.push([0,0,k,0])

    for (let i =0; i<queue.length; i++) {
        const frontNode = queue[i]
        const kLeft = frontNode[2]
        const curRow = frontNode[0]
        const curCol = frontNode[1]
        const curDistanceTraveled = frontNode[3]

        if (curRow === targetRow && curCol === targetCol) {
            console.log(uniquePaths)
            return curDistanceTraveled
        }

        for (let dir of directions) {
            const dRow = curRow + dir[0]
            const dCol = curCol + dir[1]
            if (!(dRow >= 0 && dRow < rowMax && dCol >= 0 && dCol < colMax)) continue
            const newK = kLeft - grid[dRow][dCol] 
            if (newK < 0) continue
            const dKey = `${dRow}-${dCol}-${newK}`
            if (uniquePaths[dKey]) continue
            uniquePaths[dKey] = true
            queue.push([dRow, dCol, newK, curDistanceTraveled+1])
        }
    }  

    console.log(uniquePaths)

    return -1
    
};

const shortestPathHard = (grid, k) => {
    
    const targetRow = grid.length-1
    const targetCol = grid[0].length-1
    const rowMax = grid.length
    const colMax = grid[0].length
    const queue = []
    const directions = [[1,0], [0,1], [-1,0], [0,-1]]
    const uniquePaths = {}

    queue.push([0,0,k,0])

    while (queue.length > 0) {
        const frontNode = queue.shift()
        const kLeft = frontNode[2]
        const curRow = frontNode[0]
        const curCol = frontNode[1]
        const curDistanceTraveled = frontNode[3]
        const key = `${curRow}-${curCol}-${kLeft}`

        if (curRow === targetRow && curCol === targetCol) {
            console.log(uniquePaths)
            return curDistanceTraveled
        }

        if (uniquePaths[key] === undefined ) {
            uniquePaths[key] = curDistanceTraveled
        } else {
            uniquePaths[key] = Math.min(uniquePaths[key], curDistanceTraveled)
        }

        for (let dir of directions) {
            // console.log("called")
            const dRow = curRow + dir[0]
            const dCol = curCol + dir[1]
            // if not wall
            if (dRow >= 0 && dRow < rowMax && dCol >= 0 && dCol < colMax && grid[dRow][dCol] === 0) {
                const dKey = `${dRow}-${dCol}-${kLeft}`
                if (uniquePaths[dKey] === undefined) {
                    queue.push([dRow, dCol, kLeft, curDistanceTraveled+1])
                }
            // if wall
            } else if (dRow >= 0 && dRow < rowMax && dCol >= 0 && dCol < colMax && grid[dRow][dCol] === 1 && kLeft > 0) {
                const dKey = `${dRow}-${dCol}-${kLeft}`
                if (uniquePaths[dKey] === undefined) {
                    queue.push([dRow, dCol, kLeft-1, curDistanceTraveled+1])
                }
            }
        }
    }
    console.log(uniquePaths)
    return -1
    
};



